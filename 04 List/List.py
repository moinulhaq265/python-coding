#Why Lists?
# Key Characteristics
# Ordered: The items stay in the order you put them in.
# Mutable: You can change, add, or remove items after the list is created (unlike strings!).
# Heterogeneous: You can mix data types (e.g., [1, "Apple", 3.14]).

# Syntax
mylist = ["moin",265,9.8,True]
print (mylist)
print(type(mylist))

fruits = ["apple", "banana", "mango"]
numbers = [1, 2, 3, 4, 5]
mixed = [1, "Ali", 3.14, True]   # can store different types
print (fruits)
print (numbers)
print (mixed)

# nested list --> list inside list

matrix = [[1, 2], [3, 4], [5, 6]]
print (matrix)
print (len(matrix))