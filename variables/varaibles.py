# A variable is like a container/box that stores a value in memory. You give it a name, and Python remembers the value for you.
# syntax: variable_name = value

name = "moin ul haq"
print(name)

# rules for naming variables
# 1. variable name can start with a letter or underscore
# 2. variable name can not start with a number
# 3. variable name can not contain spaces
# 4. variable name can not contain special characters
# 5. variable name is case sensitive
# 6. variable name can not be a keyword

name = "moin Ul Haq"
age = 23
height = 5.7
is_student = True
print (name)
print (age)
print (height)
print (is_student)
