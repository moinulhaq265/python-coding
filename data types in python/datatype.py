# A data type tells Python what kind of value a variable holds — a number, text, true/false, etc.

# Numeric Types
x = 10        # int   → whole numbers
y = 3.14      # float → decimal numbers
z = 2 + 3j    # complex → real + imaginary

# String (Text)
name = "Ali"          # double quotes
city = 'Karachi'      # single quotes
msg  = """Hello
World"""              # multi-line string

# Boolean
is_student = True
is_expired = False

# List
fruits = ["apple", "banana", "mango"]  # ordered, changeable

# Tuple
colors = ("red", "green", "blue")  # ordered, NOT changeable

# Set
numbers = {1, 2, 3, 4}  # unordered, no duplicates

# Dictionary
student = {"name": "Ali", "age": 16}  # key → value pairs

# Non Type
result = None   # means "no value" or "empty"